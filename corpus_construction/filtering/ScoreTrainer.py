class EvalLoopOutput(NamedTuple):
    scores: Union[np.ndarray, Tuple[np.ndarray]]
    predictions: Union[np.ndarray, Tuple[np.ndarray]]
    label_ids: Optional[Union[np.ndarray, Tuple[np.ndarray]]]
